# operasi aritmatika x : + -

# operasi penjumlahan +
a = 25
b = 31
hasil = a + b
print(a, "+", b, "=", hasil)

# operasi pengurangan -
hasil = a - b
print(a, "-", b, "=", hasil)

# operasi pembagian /
hasil = a / b
print(a, "/", b, "=", hasil)

# operasi perkalian *
hasil = a * b
print(a, "*", b, "=", hasil)

# operasi pangkat **
hasil = a ** b
print(a, "**", b, "=", hasil)

# operasi modulus % (sisa bagi)
hasil = a % b
print(a, "%", b, "=", hasil)

# operasi floor division // (pembulatan perkalian)
hasil = a // b
print(a, "//", b, "=", hasil)

# operasi gabungan aritmatika
gabung = 12 - 6 / 2 * 5 ** 2 + 1 % 3 // 4
print(gabung)

